namespace LeetCodeNet.DataStructs
{
    /// <summary>
    /// Class of node for LinkedList tasks
    /// </summary>
    public class ListNode
    {
        /// <summary>
        /// Value
        /// </summary>
        public int val;

        /// <summary>
        /// Link to next node
        /// </summary>
        public ListNode next;

        /// <summary>
        /// Constructor
        /// </summary>
        /// <param name="x"> Value </param>
        public ListNode(int x)
        {
            val = x;
        }
    }
}
